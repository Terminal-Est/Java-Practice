public class ObjectArray2 {
	private int a;
	private int b;
	private int c;
	
	public ObjectArray2(int x, int y, int z) {
		this.a = x;
		this.b = y;
		this.c = z;
	}

	public int getX (int x) {
		x = this.a;
		return x;
	}
	
	public int getY (int y) {
		y = this.b;
		return y;
	}
	
	public int getZ (int z) {
		z = this.c;
		return z;
	}
	
	
}
